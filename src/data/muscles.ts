export const muscles: Muscle[] = RAW;

export const muscleById: Record<MuscleId, Muscle> = Object.fromEntries(
  RAW.map((m) => [m.id, m]),
);

export const featuredMuscles: Muscle[] = RAW.filter((m) => m.featured);

/** Safe lookup — returns the muscle or undefined without throwing. */
export function getMuscle(id: MuscleId): Muscle | undefined {
  return muscleById[id];
}

/** Human-readable name for an id, falling back to a de-slugged label. */
export function muscleName(id: MuscleId): string {
  return (
    muscleById[id]?.name ??
    id.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase())
  );
}
